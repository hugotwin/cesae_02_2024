package calculadora;

public class Calculadora {

    private double valor1;
    private double valor2;



    public double  multip(double valor1, double valor2)
    {
        this.valor1= valor1;
        this.valor2= valor2;

        return this.valor1*this.valor2;

    }
    public double  soma(double valor1, double valor2)
    {
        this.valor1= valor1;
        this.valor2= valor2;

        return this.valor1+this.valor2;

    }

    public double  subtra(double valor1, double valor2)
    {
        this.valor1= valor1;
        this.valor2= valor2;

        return this.valor1-this.valor2;

    }

    public double  div(double valor1, double valor2)
    {
        this.valor1= valor1;
        this.valor2= valor2;

        return this.valor1-this.valor2;

    }





}


